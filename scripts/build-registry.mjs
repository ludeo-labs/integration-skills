#!/usr/bin/env node
// Regenerate registry.json from skills/*/SKILL.md + package.json.
// With --check, fails if registry.json is out of date instead of writing it.
//
// Usage:
//   node scripts/build-registry.mjs            # write registry.json
//   node scripts/build-registry.mjs --check    # CI: verify it's current

import { readdirSync, readFileSync, writeFileSync, existsSync, statSync } from 'node:fs';
import { join, dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const SKILLS_DIR = join(ROOT, 'skills');
const REGISTRY_PATH = join(ROOT, 'registry.json');
const CHECK = process.argv.includes('--check');

const ENGINE_HINTS = [
  [/unreal/i, 'unreal'],
  [/unity/i, 'unity'],
];

function parseFrontmatter(md) {
  const m = md.match(/^---\n([\s\S]*?)\n---/);
  if (!m) return {};
  const fm = {};
  for (const line of m[1].split('\n')) {
    const kv = line.match(/^([A-Za-z0-9_.]+):\s*(.*)$/);
    if (kv) fm[kv[1]] = kv[2].trim();
  }
  return fm;
}

function engineOf(name, fm) {
  for (const [re, val] of ENGINE_HINTS) {
    if (re.test(name) || re.test(fm.description || '')) return val;
  }
  return 'any';
}

const skills = [];
if (existsSync(SKILLS_DIR)) {
  for (const folder of readdirSync(SKILLS_DIR).sort()) {
    const skillDir = join(SKILLS_DIR, folder);
    if (!statSync(skillDir).isDirectory()) continue;
    const skillMd = join(skillDir, 'SKILL.md');
    if (!existsSync(skillMd)) continue;

    const fm = parseFrontmatter(readFileSync(skillMd, 'utf8'));
    let version = fm['metadata.version'] || fm.version || null;
    const pkgPath = join(skillDir, 'package.json');
    if (!version && existsSync(pkgPath)) {
      version = JSON.parse(readFileSync(pkgPath, 'utf8')).version || null;
    }

    skills.push({
      name: fm.name || folder,
      path: `skills/${folder}`,
      engine: engineOf(folder, fm),
      version,
      description: fm.description || '',
      status: version ? 'released' : 'scaffolded',
    });
  }
}

const registry = {
  $comment: 'GENERATED by scripts/build-registry.mjs — do not edit by hand. Run `npm run build-registry`.',
  schemaVersion: 1,
  generatedAt: null, // stamped by CI to keep local runs deterministic
  skills,
};

const next = JSON.stringify(registry, null, 2) + '\n';

if (CHECK) {
  const current = existsSync(REGISTRY_PATH) ? readFileSync(REGISTRY_PATH, 'utf8') : '';
  // ignore generatedAt drift when comparing
  const norm = (s) => s.replace(/"generatedAt":\s*"[^"]*"/, '"generatedAt": null');
  if (norm(current) !== norm(next)) {
    console.error('registry.json is out of date. Run `npm run build-registry`.');
    process.exit(1);
  }
  console.log('registry.json is up to date.');
} else {
  writeFileSync(REGISTRY_PATH, next);
  console.log(`Wrote registry.json (${skills.length} skill(s)).`);
}
